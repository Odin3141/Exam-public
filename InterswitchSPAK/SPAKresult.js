function addClass() {
  let newClass = "speed-" + speed;
  let prevClass = "speed-" + prevSpeed;
  let el = document.getElementsByClassName("arrow-wrapper")[0];
  if (el.classList.contains(prevClass)) {
      el.classList.remove(prevClass);
      el.classList.add(newClass);
  }
  prevSpeed = speed;
}
